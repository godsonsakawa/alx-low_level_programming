#include <stdio.h>

/**
  * add - addition.
  * @a: first integer
  * @b: second integer
  * Return: Always 0 Success.
  */

int add(int a, int b)
{
	return (a + b);
}

/**
  * sub - subtraction
  * @a: first integer
  * @b: second integer
  * Return: Always 0 success.
  */

int sub(int a, int b)
{
	return (a - b);
}

/**
  * mul - multiplication
  * @a: first integer
  * @b: second integer
  * Return: Always 0 success.
  */

int mul(int a, int b)
{
	return (a * b);
}

/**
  * div - division
  * @a: first integer
  * @b: second integer
  * Return: Always 0 success.
  */

int div(int a, int b)
{
	return (a / b);
}

/**
  * mod - modulus
  * @a: first integer
  * @b: second integer
  * Return: Always 0 success.
  */

int mod(int a, int b)
{
	return (a % b);
}
